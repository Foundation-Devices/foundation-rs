use anyhow::{Error, Result};
use heapless::{String, Vec};
use serde::Deserialize;

/// # Parse Response ID
///
/// In order to know which kind of response we are dealing with, we need to parse the response ID.
///
/// ## Parameters
///
/// resp: The slice response to parse.
///
/// ## Examples
/// ```
/// use stratum_v1::client_resp::parse_response_id;
///
/// let resp = br#"{"id": 1, "result": [ [ ["mining.set_difficulty", "b4b6693b72a50c7116db18d6497cac52"], ["mining.notify", "ae6812eb4cd7735a302a8a9dd95cf71f"]], "08000002", 4], "error": null}"#;
/// assert_eq!(parse_response_id(resp).unwrap(), Some(1));
///
/// let resp = br#"{"error":null,"id":2,"result":[[["mining.notify","e26e1928"]],"e26e1928",4]}"#;
/// assert_eq!(parse_response_id(resp).unwrap(), Some(2));
///
/// let resp = br#"{ "id": null, "method": "mining.set_difficulty", "params": [2]}"#;
/// assert_eq!(parse_response_id(resp).unwrap(), None);
///
/// let resp = br#"{ "id": "ab", "method": "mining.set_difficulty", "params": [2]}"#;
/// assert!(parse_response_id(resp).is_err());
/// ```
pub fn parse_response_id(resp: &[u8]) -> Result<Option<u64>> {
    #[derive(Deserialize)]
    struct IdOnly {
        id: Option<u64>,
    }
    let id = serde_json_core::from_slice::<IdOnly>(resp)
        .map_err(Error::msg)?
        .0
        .id;
    match id {
        None => Ok(None),
        Some(id) => Ok(Some(id)),
    }
}

