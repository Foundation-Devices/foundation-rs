/// # Parse Response Configure
///
/// Parses the response to the `configure` method.
///
/// ## Parameters
///
/// resp: The response slice to parse.
///
/// ## Examples
/// ```
/// use stratum_v1::client_resp::{ConfigureResp, parse_response_configure};
/// use heapless::String;
///
/// let resp = br#"{"error": null,"id": 1,"result": {"version-rolling": true,"version-rolling.mask": "18000000","minimum-difficulty": true}}"#;
/// assert_eq!(parse_response_configure(resp).unwrap(), ConfigureResp {
///     version_rolling: Some(true),
///     version_rolling_mask: Some(String::from("18000000")),
///     version_rolling_min_bit_count: None,
///     minimum_difficulty: Some(true),
///     minimum_difficulty_value: None,
///     subscribe_extranonce: None,
///     info: None,
///     info_connection_url: None,
///     info_hw_version: None,
///     info_sw_version: None,
///     info_hw_id: None
/// });
/// ```
pub fn parse_response_configure(resp: &[u8]) -> Result<ConfigureResp> {
    serde_json_core::from_slice::<json_rpc_types::Response<ConfigureResp, String<64>>>(resp)
        .map_err(Error::msg)?
        .0
        .payload
        .map_err(Error::msg)
}

#[derive(Debug, Deserialize, PartialEq)]
pub struct ConnectResp(
    // Subscriptions details - 2-tuple with name of subscribed notification and subscription ID. Teoretically it may be used for unsubscribing, but obviously miners won't use it.
    Vec<Vec<String<32>, 2>, 2>,
    // Extranonce1 - Hex-encoded, per-connection unique string which will be used for coinbase serialization later. Keep it safe!
    String<8>,
    // Extranonce2_size - Represents expected length of extranonce2 which will be generated by the miner.
    usize,
);

impl ConnectResp {
    pub fn subscriptions(&self) -> &Vec<Vec<String<32>, 2>, 2> {
        &self.0
    }

    pub fn extranonce1(&self) -> u32 {
        let v: Vec<u8, 4> = hex::decode_heapless(&self.1).expect("decode error");
        u32::from_be_bytes(v[0..4].try_into().unwrap())
    }

    pub fn extranonce2_size(&self) -> usize {
        self.2
    }
}

